    private void btnTimkiemnguoithueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemnguoithueActionPerformed
        // TODO add your handling code here:
        String tukhoa= txtTukhoaNguoithue.getText();
        if (tukhoa.equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập từ khóa!!");
        }
        else{
            tbKhachHang khach = new tbKhachHang();
            Vector<clsKhachHang> ds = khach.SearchKhachhang(tukhoa);
            if (ds.size()>0) {
                DefaultTableModel tb = (DefaultTableModel)tableNguoithue.getModel();
                tb.setRowCount(0);
                
                for (clsKhachHang kh : ds) {
                    tb.addRow(new Object[]{kh.getMaKH(),kh.getName(),kh.getPhone()});
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Không có kết quả tìm kiếm nào!!");
            }
            
            
        }
    }//GEN-LAST:event_btnTimkiemnguoithueActionPerformed

