    private void btnTimkiemdausachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemdausachActionPerformed
        // TODO add your handling code here:
        String tukhoa= txtTukhoaSach.getText();
        if (tukhoa.equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập từ khóa!!");
        }
        else{
            tbSach sach = new tbSach();
            Vector<clsSach> ds = sach.SearchBook(tukhoa, 0, "", 0, 0);
            
            if (ds.size()>0) {
                DefaultTableModel tb = (DefaultTableModel)tableSachthue.getModel();
                tb.setRowCount(0);
                
                for (clsSach d : ds) {
                    tb.addRow(new Object[]{d.getMaSach(),d.getTenSach(),d.getGiaTien()});
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Không có kết quả tìm kiếm nào!!");
            }
            
            
        }
    }//GEN-LAST:event_btnTimkiemdausachActionPerformed

